#!/usr/bin/env node
/**
 * Generate a fresh Ed25519 device keypair for the MCP gateway.
 *
 * Outputs two values to .env (or stdout with --stdout):
 *
 *   OPENCLAW_DEVICE_IDENTITY
 *     JSON-encoded DeviceIdentity for the MCP gateway container.
 *
 *   OPENCLAW_DEVICE_PAIRING
 *     JSON-encoded pairing record for the OpenClaw Gateway container.
 *     Written verbatim to $OPENCLAW_STATE_DIR/devices/paired.json.
 *
 * Usage:
 *   node generate-device-pairing.mjs            # appends to .env
 *   node generate-device-pairing.mjs --stdout   # prints to stdout
 */

import { createHash, generateKeyPairSync, randomBytes } from "node:crypto";
import { appendFileSync } from "node:fs";

const ED25519_SPKI_PREFIX = Buffer.from("302a300506032b6570032100", "hex");

function toBase64Url(buf) {
  return buf.toString("base64").replace(/\+/g, "-").replace(/\//g, "_").replace(/=+$/, "");
}

const { publicKey, privateKey } = generateKeyPairSync("ed25519");
const spkiDer = publicKey.export({ type: "spki", format: "der" });
const rawPublicKey = spkiDer.subarray(ED25519_SPKI_PREFIX.length);
const deviceId = createHash("sha256").update(rawPublicKey).digest("hex");
const publicKeyRaw = toBase64Url(rawPublicKey);
const privateKeyPem = privateKey.export({ type: "pkcs8", format: "pem" });

const deviceIdentity = { deviceId, publicKeyRaw, privateKeyPem };

const nowMs = Date.now();
const pairedRecord = {
  deviceId,
  publicKey: publicKeyRaw,
  role: "operator",
  roles: ["operator"],
  scopes: ["operator.admin", "operator.read"],
  approvedScopes: ["operator.admin", "operator.read"],
  tokens: {
    operator: {
      token: randomBytes(32).toString("base64url"),
      role: "operator",
      scopes: ["operator.admin", "operator.read"],
      createdAtMs: nowMs
    }
  },
  createdAtMs: nowMs,
  approvedAtMs: nowMs
};

const devicePairing = { [deviceId]: pairedRecord };

const identityJson = JSON.stringify(JSON.stringify(deviceIdentity));
const pairingJson = JSON.stringify(JSON.stringify(devicePairing));

if (process.argv.includes("--stdout")) {
  console.log(`OPENCLAW_DEVICE_IDENTITY=${identityJson}`);
  console.log(`OPENCLAW_DEVICE_PAIRING=${pairingJson}`);
} else {
  const lines = [
    "",
    "# MCP Gateway Device Pairing (generated by generate-device-pairing.mjs)",
    `OPENCLAW_DEVICE_IDENTITY=${identityJson}`,
    `OPENCLAW_DEVICE_PAIRING=${pairingJson}`,
    ""
  ].join("\n");
  appendFileSync(".env", lines, { mode: 0o600 });
  console.log(`Device pairing appended to .env (deviceId=${deviceId.slice(0, 16)}…)`);
}
